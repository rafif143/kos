export function createI18n() {
    let locale = $state('id');

    return {
        get locale() { return locale; },
        set locale(v) { locale = v; },
        toggle() { locale = locale === 'id' ? 'en' : 'id'; },
        t(key) {
            return translations[locale]?.[key] ?? translations['en']?.[key] ?? key;
        },
    };
}

export const i18n = createI18n();
